package mysql

import (
	"testing"
)

var readStringTests = []struct {
	in       []byte
	charSet  charSet
	expected string
}{
	{
		[]byte{
			0x14, 0x00, 0x00, 0x00, 0x03, 0x53, 0x45, 0x4c, 0x45, 0x43, 0x54, 0x20, 0x27, 0x82, 0xa0, 0x82,
			0xa2, 0x82, 0xa4, 0x82, 0xa6, 0x82, 0xa8, 0x27,
		},
		charSetSjis,
		"SELECT 'あいうえお'",
	},
	{
		[]byte{
			0x14, 0x00, 0x00, 0x00, 0x03, 0x53, 0x45, 0x4c, 0x45, 0x43, 0x54, 0x20, 0x27, 0xa4, 0xa2, 0xa4,
			0xa4, 0xa4, 0xa6, 0xa4, 0xa8, 0xa4, 0xaa, 0x27,
		},
		charSetUjis,
		"SELECT 'あいうえお'",
	},
	{
		[]byte{
			0x14, 0x00, 0x00, 0x00, 0x03, 0x73, 0x65, 0x6c, 0x65, 0x63, 0x74, 0x20, 0x2a, 0x20, 0x66, 0x72,
			0x6f, 0x6d, 0x20, 0x70, 0x6f, 0x73, 0x74, 0x73,
		},
		charSetUnknown,
		"select * from posts",
	},
}

func TestReadString(t *testing.T) {
	for _, tt := range readStringTests {
		actual := readString(tt.in[5:], tt.charSet)
		if actual != tt.expected {
			t.Errorf("actual %#v\nwant %#v", actual, tt.expected)
		}
	}
}
